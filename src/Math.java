public class Math {
    public static void main(String[] args) {
        System.out.println(java.lang.Math.abs(-129));//取绝对值
        System.out.println(java.lang.Math.max(12, 13));//取最大值
        System.out.println(java.lang.Math.random() * 10 + 10);//获取随机数
        System.out.println(java.lang.Math.pow(2,3));//前值的后值次方
        System.out.println(java.lang.Math.sqrt(16));//平方根
        System.out.println(java.lang.Math.round(3.14));//四舍五入
    }
}
